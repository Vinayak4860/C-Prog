#include <stdio.h>

int main() {
    // Newline character (\n)
    printf("Hello, World!\n");
    
    // Tab character (\t)
    printf("This is a\ttab character.\n");
    
    // Carriage return (\r)
    printf("Carriage\rreturn\n");
    
    // Backspace (\b)
    printf("Backs\bpace\n");
    
    // Hexadecimal character representation (\x)
    printf("Hexadecimal: \x41 \x42 \x43\n");
    
    // Double backslash (\\)
    printf("Double backslash: \\n \\t \\r\n");
    
    // Double quotes (\")
    printf("Double quotes: \"Hello, World!\"\n");
    
    return 0;
}
